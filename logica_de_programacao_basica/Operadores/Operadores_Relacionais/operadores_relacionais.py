# Revisão de Operadores Relacionais em Python

# Definindo duas variáveis
a = 10
b = 20

print("a =", a)
print("b =", b)

# Comparações
print("\nOperadores relacionais:")
print("a == b:", a == b)   # Igual a
print("a != b:", a != b)   # Diferente de
print("a > b:", a > b)     # Maior que
print("a < b:", a < b)     # Menor que
print("a >= b:", a >= b)   # Maior ou igual a
print("a <= b:", a <= b)   # Menor ou igual a
